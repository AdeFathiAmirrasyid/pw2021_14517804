<?php
// Pengulangan
// for 
// while
// do..while
// foreach : perulangan khusus array

//  Perulangan menggunakan for 
for($i = 0; $i < 5; $i++) {
    echo "Hello World! <br>";
}

// Perulangan menggunakan While
$i = 0;
while($i < 5){
echo "Hello World? <br>";
$i++;
}

$j = 50;
do{
    echo "$j <br>";
    $j++;
}while($j < 10);

?>